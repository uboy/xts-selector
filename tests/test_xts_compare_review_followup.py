class PhaseFourTests(unittest.TestCase):
    def setUp(self):
        self.parser = build_parser()

    def test_open_archive_missing_path_raises_file_not_found(self):
        with self.assertRaises(FileNotFoundError):
            open_archive("/definitely/missing/xts.zip")

    def test_failure_type_unknown_alias_is_preserved(self):
        self.assertIs(FailureType.UNKNOWN, FailureType.UNKNOWN_FAIL)
        self.assertEqual(FailureType.UNKNOWN.value, "UNKNOWN")

    def test_load_run_warns_on_duplicate_test_identity(self):
        tmp = tempfile.mkdtemp()
        try:
            report_dir = Path(tmp) / "report"
            report_dir.mkdir()
            (report_dir / "summary_report.xml").write_text(
                """<testsuites name="ActsDup">
<testsuite name="Suite">
  <testcase name="case1" status="run" result="true" />
  <testcase name="case1" status="run" result="false" />
</testsuite>
</testsuites>
""",
                encoding="utf-8",
            )
            with warnings.catch_warnings(record=True) as caught:
                warnings.simplefilter("always")
                load_run(str(report_dir))
            self.assertTrue(any("Duplicate test identity" in str(item.message) for item in caught))
        finally:
            shutil.rmtree(tmp, ignore_errors=True)

    def test_main_auto_generates_html_output_path(self):
        tmp = tempfile.mkdtemp()
        try:
            base_dir = Path(tmp) / "base"
            target_dir = Path(tmp) / "target"
            base_dir.mkdir()
            target_dir.mkdir()
            _write_valid_summary_xml(base_dir / "summary_report.xml")
            _write_valid_summary_xml(target_dir / "summary_report.xml")

            stderr = io.StringIO()
            with mock.patch("sys.stdout", new=io.StringIO()), mock.patch("sys.stderr", new=stderr):
                code = main([str(base_dir), str(target_dir), "--html"])

            self.assertEqual(code, 0)
            self.assertIn("HTML output requires a file. Writing to:", stderr.getvalue())
        finally:
            for html_file in Path.cwd().glob("xts_compare_*.html"):
                html_file.unlink(missing_ok=True)
            shutil.rmtree(tmp, ignore_errors=True)

    def test_format_report_regressions_only_shows_only_regression_section(self):
        base_meta, base_results = _make_run(
            [
                _make_result("M", "S", "regression_case", TestOutcome.PASS),
                _make_result("M", "S", "improvement_case", TestOutcome.FAIL, "old"),
            ]
        )
        target_meta, target_results = _make_run(
            [
                _make_result("M", "S", "regression_case", TestOutcome.FAIL, "boom"),
                _make_result("M", "S", "improvement_case", TestOutcome.PASS),
            ]
        )
        report = compare_runs(base_meta, base_results, target_meta, target_results)
        text = format_report(report, regressions_only=True)
        self.assertIn("REGRESSIONS", text)
        self.assertNotIn("IMPROVEMENTS", text)
        self.assertNotIn("Module Health", text)

    def test_format_report_emits_failure_type_tip(self):
        base_meta, base_results = _make_run(
            [
                _make_result("M", "S", "c1", TestOutcome.PASS),
                _make_result("M", "S", "c2", TestOutcome.PASS),
            ]
        )
        target_meta, target_results = _make_run(
            [
                TestResult(
                    identity=TestIdentity("M", "S", "c1"),
                    outcome=TestOutcome.FAIL,
                    message="App died",
                    failure_type=FailureType.CRASH,
                ),
                TestResult(
                    identity=TestIdentity("M", "S", "c2"),
                    outcome=TestOutcome.FAIL,
                    message="App died",
                    failure_type=FailureType.CRASH,
                ),
            ]
        )
        report = compare_runs(base_meta, base_results, target_meta, target_results)
        text = format_report(report)
        self.assertIn("Use --failure-type crash", text)

    def test_format_report_emits_stable_blocked_tip(self):
        base_meta, base_results = _make_run([_make_result("M", "S", "c1", TestOutcome.BLOCKED)])
        target_meta, target_results = _make_run([_make_result("M", "S", "c1", TestOutcome.BLOCKED)])
        report = compare_runs(base_meta, base_results, target_meta, target_results)
        text = format_report(report)
        self.assertIn("Use --show-stable-blocked", text)

    def test_format_report_can_show_stable_blocked_section(self):
        base_meta, base_results = _make_run([_make_result("M", "S", "c1", TestOutcome.BLOCKED)])
        target_meta, target_results = _make_run([_make_result("M", "S", "c1", TestOutcome.BLOCKED)])
        report = compare_runs(base_meta, base_results, target_meta, target_results)
        text = format_report(report, show_stable_blocked=True)
        self.assertIn("STABLE BLOCKED", text)

    def test_main_auto_orders_positional_compare_by_filename_timestamp(self):
        tmp = tempfile.mkdtemp()
        try:
            later_dir = Path(tmp) / "2025-12-25-16-48-38"
            earlier_dir = Path(tmp) / "2025-12-11-17-10-38"
            later_dir.mkdir()
            earlier_dir.mkdir()
            _write_valid_summary_xml(later_dir / "summary_report.xml")
            _write_valid_summary_xml(earlier_dir / "summary_report.xml")

            stdout = io.StringIO()
            stderr = io.StringIO()
            with mock.patch("sys.stdout", new=stdout), mock.patch("sys.stderr", new=stderr):
                code = main([str(later_dir), str(earlier_dir)])

            self.assertEqual(code, 0)
            self.assertIn("Auto-detected compare order", stderr.getvalue())
            self.assertIn("2025-12-11-17-10-38", stdout.getvalue())
            self.assertIn("2025-12-25-16-48-38", stdout.getvalue())
        finally:
            shutil.rmtree(tmp, ignore_errors=True)


