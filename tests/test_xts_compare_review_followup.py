def _make_result(module: str, suite: str, case: str, outcome: TestOutcome, msg: str = "") -> TestResult:
    return TestResult(
        identity=TestIdentity(module=module, suite=suite, case=case),
        outcome=outcome,
        message=msg,
    )


def _make_run(results: list[TestResult]):
    result_map = {result.identity: result for result in results}
    pass_count = sum(1 for result in result_map.values() if result.outcome == TestOutcome.PASS)
    fail_count = sum(1 for result in result_map.values() if result.outcome == TestOutcome.FAIL)
    blocked_count = sum(1 for result in result_map.values() if result.outcome == TestOutcome.BLOCKED)
    from arkui_xts_selector.xts_compare.models import RunMetadata

    return RunMetadata(
        label="run",
        total_tests=len(result_map),
        pass_count=pass_count,
        fail_count=fail_count,
        blocked_count=blocked_count,
    ), result_map


def _write_valid_summary_xml(path: Path) -> None:
    path.write_text(
        """<testsuites name="ActsSample">
<testsuite name="Suite">
  <testcase name="case1" status="run" result="true" time="0.1" />
</testsuite>
</testsuites>
""",
        encoding="utf-8",
    )


def _make_zip_report(path: Path) -> None:
    with zipfile.ZipFile(path, "w") as archive:
        archive.writestr(
            "summary_report.xml",
            """<testsuites name="ActsZip">
<testsuite name="Suite">
  <testcase name="case1" status="run" result="true" time="0.1" />
</testsuite>
</testsuites>
""",
        )


def _make_tar_report(path: Path) -> None:
    tmp = tempfile.mkdtemp()
    try:
        report_root = Path(tmp) / "report"
        report_root.mkdir()
        _write_valid_summary_xml(report_root / "summary_report.xml")
        with tarfile.open(path, "w:gz") as archive:
            archive.add(report_root / "summary_report.xml", arcname="summary_report.xml")
    finally:
        shutil.rmtree(tmp, ignore_errors=True)


class SecurityRegressionTests(unittest.TestCase):
    def test_load_run_rejects_path_traversal_in_log_refs(self):
        tmp = tempfile.mkdtemp()
        try:
            archive_dir = Path(tmp) / "archive"
            archive_dir.mkdir()
            secret_dir = Path(tmp) / "secret"
            secret_dir.mkdir()

            (archive_dir / "summary_report.xml").write_text(
                """<testsuites name="ActsCrashTest">
<testsuite name="CrashSuite">
  <testcase name="case1" status="run" result="false" />
</testsuite>
</testsuites>
""",
                encoding="utf-8",
            )
            static_dir = archive_dir / "static"
            static_dir.mkdir()
            (static_dir / "data.js").write_text(
                "window.reportData = "
                + json.dumps(
                    {
                        "modules": [
                            {
                                "name": "ActsCrashTest",
                                "error": "App died",
                                "logs": {
                                    "crash_log": "../secret/cppcrash.log",
                                },
                            }
                        ]
                    }
                )
                + ";",
                encoding="utf-8",
            )
            (secret_dir / "cppcrash.log").write_text(
                "Generated by HiviewDFX@OpenHarmony\nReason:Signal:SIGSEGV(SEGV_MAPERR)@0x1\n",
                encoding="utf-8",
            )

            meta, _results = load_run(str(archive_dir))
            self.assertIn("ActsCrashTest", meta.module_infos)
            self.assertIsNone(meta.module_infos["ActsCrashTest"].crash_info)
        finally:
            shutil.rmtree(tmp, ignore_errors=True)


class PhaseOneLogicTests(unittest.TestCase):
    def test_compute_module_health_counts_unblocked_as_positive(self):
        module = ModuleComparison(
            module="ActsModule",
            counts={
                TransitionKind.UNBLOCKED.value: 3,
            },
        )
        self.assertAlmostEqual(compute_module_health(module), 100.0, places=1)

    def test_selector_correlation_unblocked_does_not_report_no_change(self):
        base_meta, base_results = _make_run(
            [_make_result("ActsButtonTest", "ButtonSuite", "case1", TestOutcome.BLOCKED)]
        )
        target_meta, target_results = _make_run(
            [_make_result("ActsButtonTest", "ButtonSuite", "case1", TestOutcome.PASS)]
        )
        report = compare_runs(base_meta, base_results, target_meta, target_results)
        selector_report = {
            "results": [
                {
                    "changed_file": "frameworks/core/components/button/button_pattern.cpp",
                    "projects": [
                        {
                            "project": "test/xts/acts/arkui/ace_ets_component_button_static",
                            "score": 10,
                            "confidence": "high",
                            "bucket": "must-run",
                            "variant": "static",
                        }
                    ],
                }
            ]
        }

        correlations = correlate_with_selector(report, selector_report)
        self.assertEqual(len(correlations), 1)
        self.assertFalse(correlations[0].predicted_projects[0].predicted_but_no_change)

    def test_blocked_to_fail_is_new_fail(self):
        transition = classify_transition(
            _make_result("M", "S", "C", TestOutcome.BLOCKED),
            _make_result("M", "S", "C", TestOutcome.FAIL),
        )
        self.assertEqual(transition, TransitionKind.NEW_FAIL)

    def test_blocked_to_blocked_is_stable_blocked(self):
        transition = classify_transition(
            _make_result("M", "S", "C", TestOutcome.BLOCKED),
            _make_result("M", "S", "C", TestOutcome.BLOCKED),
        )
        self.assertEqual(transition, TransitionKind.STABLE_BLOCKED)


